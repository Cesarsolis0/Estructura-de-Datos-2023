// Metodos para trabajar con listas en dart:
 add(): Agrega un elemento al final de la lista.
 addAll(): Agrega todos los elementos de un iterable al final de la lista.
 insert(): Inserta un elemento en una posición específica de la lista.
 insertAll(): Inserta todos los elementos de un iterable en una posición específica de la lista.
 remove(): Elimina el primer elemento de la lista que coincide con el objeto dado.
 removeAt(): Elimina el elemento en la posición dada de la lista y devuelve su valor.
 removeLast(): Elimina el último elemento de la lista y devuelve su valor.
 removeRange(): Elimina los elementos de la lista entre los índices dados.
 clear(): Elimina todos los elementos de la lista.
 sort(): Ordena de forma asendente los elementos de una lista.
 
